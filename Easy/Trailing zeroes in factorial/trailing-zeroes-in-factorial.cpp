//User function Template for C++
class Solution
{
public:
    int trailingZeroes(int N)
    {
        int num=N,ans=0;
        while(num>=5)
        {
          ans+=num/5;
          num=num/5;  
        }
        return ans;
    }
};

//{ Driver Code Starts.
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        int N;
        cin >> N;
        Solution ob;
        int ans  = ob.trailingZeroes(N);
        cout<<ans<<endl;
    }
    return 0;
}
